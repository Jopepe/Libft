#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	a;
	int		s;

	a = 0;
	s = 1;
	if (n < 0)
	{
		s = -1;
		write(fd, "-", 1);
	}
	if (n > 9 || n < -9)
	{
		ft_putnbr_fd(s * (n / 10), fd);
	}
	a = s * (n % 10) + '0';
	write(fd, &a, 1);
}
